package basicjava;

public class typecasting {

	public static void main(String[] args) {
		double mydouble = 8.36;
		int myint = (int) mydouble;
		System.out.println(myint);// implicit casting mannually done
		
		
		int mystring = 100;
		long mylong = mystring;
		System.out.println(mylong);
		
		
	}

}
